// Helper function to get block explorer URL
export const getBlockExplorerUrl = (chainId: number, txHash: string): string => {
  switch (chainId) {
    case 1: // Ethereum Mainnet
      return `https://etherscan.io/tx/${txHash}`;
    case 11155111: // Sepolia
      return `https://sepolia.etherscan.io/tx/${txHash}`;
    case 137: // Polygon Mainnet
      return `https://polygonscan.com/tx/${txHash}`;
    case 80001: // Polygon Mumbai
      return `https://mumbai.polygonscan.com/tx/${txHash}`;
    default:
      return `https://etherscan.io/tx/${txHash}`;
  }
};

// Helper function to get NFT explorer URL
export const getNFTExplorerUrl = (chainId: number, contractAddress: string, tokenId: string): string => {
  switch (chainId) {
    case 1: // Ethereum Mainnet
      return `https://opensea.io/assets/ethereum/${contractAddress}/${tokenId}`;
    case 11155111: // Sepolia
      return `https://testnets.opensea.io/assets/sepolia/${contractAddress}/${tokenId}`;
    case 137: // Polygon Mainnet
      return `https://opensea.io/assets/matic/${contractAddress}/${tokenId}`;
    case 80001: // Polygon Mumbai
      return `https://testnets.opensea.io/assets/mumbai/${contractAddress}/${tokenId}`;
    default:
      return `https://opensea.io/assets/ethereum/${contractAddress}/${tokenId}`;
  }
};
